package variable.ex;

public class VarEx1Question {
    public static void main(String[] args) {
        // 문제 => 4, 3을 다른 숫자로 한 번에 변경할 수 있도록 num1, num2 사용하기
        System.out.println(4 + 3);
        System.out.println(4 - 3);
        System.out.println(4 * 3);

        // 답
        int num1, num2;
        num1 = 4;
        num2 = 3;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
    }
}
